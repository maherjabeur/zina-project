    public function onKernelResponse(ResponseEvent $event): void
    {
        $request = $event->getRequest();
        $response = $event->getResponse();

        if ($request->getLocale() !== 'ar' || !$this->isHtmlResponse($response)) {
            return;
        }

        $content = $response->getContent();
        if ($content === false || $content === '') {
            return;
        }

        $response->setContent($this->translateHtml($content));
    }

    private function translateHtml(string $content): string
    {
        $protectedChunks = [];

        foreach ([
            '/<(script|style)\b[^>]*>.*?<\/\1>/is',
            '/<textarea\b[^>]*>.*?<\/textarea>/is',
            '/\s(?:id|class|href|src|action|method|type|name|value|rel|content|property|lang|hreflang|dir|role|for|aria-[\w-]+|data-[\w-]+)=(["\']).*?\1/is',
        ] as $pattern) {
            $content = $this->protectChunks($content, $pattern, $protectedChunks);
        }

        $content = strtr($content, $this->buildPhraseMap());

        return strtr($content, $protectedChunks);
    }

    /**
     * @param array<string, string> $protectedChunks
     */
    private function protectChunks(string $content, string $pattern, array &$protectedChunks): string
    {
        return preg_replace_callback($pattern, static function (array $matches) use (&$protectedChunks): string {
            $token = '___BELLA_PROTECTED_' . count($protectedChunks) . '___';
            $protectedChunks[$token] = $matches[0];

            return $token;
        }, $content) ?? $content;
    }

    private function buildPhraseMap(): array
    {
        $phrases = array_replace(self::PHRASES, self::EXTRA_PHRASES);

        if (!function_exists('mb_convert_encoding')) {
            return $phrases;
        }

        foreach ($phrases as $source => $target) {
            $firstPass = @mb_convert_encoding($source, 'UTF-8', 'Windows-1252');
            if (is_string($firstPass) && $firstPass !== $source) {
                $phrases[$firstPass] = $target;

                $secondPass = @mb_convert_encoding($firstPass, 'UTF-8', 'Windows-1252');
                if (is_string($secondPass) && $secondPass !== $firstPass) {
                    $phrases[$secondPass] = $target;
                }
            }
        }

        return $phrases;
    }

    private function isHtmlResponse(Response $response): bool
    {
        $contentType = $response->headers->get('Content-Type', '');

        return str_contains($contentType, 'text/html') || $contentType === '';
    }

    public static function getSubscribedEvents(): array
    {
        return [
            KernelEvents::RESPONSE => [['onKernelResponse', -20]],
        ];
    }
